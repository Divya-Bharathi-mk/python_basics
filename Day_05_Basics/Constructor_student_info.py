class student:
    def __init__(self,name,age,mark):
        self.name=name
        self.age=age
        self.mark=mark

# Creating objects
        
obj1=student("Divya",26,788)
obj2=student("Alien",26,988)

# Printing details

print("Name : ",obj1.name)
print("Age : ",obj1.age)
print("Mark : ",obj1.mark)

print("Name : ",obj2.name)
print("Age : ",obj2.age)
print("Mark : ",obj2.mark)


